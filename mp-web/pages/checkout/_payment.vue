<template>
  <div>
    <div id="payment-page">
      <div class="container">
        <div class="payment-container">
          <div class="payment" :ref="`${type}`"></div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
let AdyenCheckout;
if (process.client) {
  AdyenCheckout = require("@adyen/adyen-web");
}

export default {
  components: {},
  data() {
    return {
      sessionId: '',
      redirectResult: '',
    }
  },
  head() {
    return {
      title: "Payment page",
    };
  },
  asyncData({ route }) {
    return { type: route.params.payment };
  },
  created() {

  },
  async mounted() {

    const urlParams = new URLSearchParams(window.location.search);
    this.sessionId = urlParams.get('sessionId');
    this.redirectResult = urlParams.get('redirectResult');

    if (this.sessionId) {
      // found session: finalize checkout
      await this.finalizeCheckout();
    } else {
      // no session: init checkout
      await this.startCheckout();
    }

  },
  methods: {
    async startCheckout() {
      try {
        // Initiate Sessions
        const {response, clientKey} = await this.callServer("/api/sessions?type=" + this.type);

        // Create AdyenCheckout using Sessions response
        const checkout = await this.createAdyenCheckout(response, clientKey);

        // Create an instance of Drop-in and mount it
        checkout.create(this.type).mount(this.$refs[this.type]);

      } catch (error) {
        console.error(error);
        alert("Error occurred. Look at console for details");
      }
    },

    async finalizeCheckout() {
      try {
        // Create AdyenCheckout re-using existing Session
        const checkout = await this.createAdyenCheckout({id: this.sessionId});

        // Submit the extracted redirectResult (to trigger onPaymentCompleted() handler)
        checkout.submitDetails({details: this.redirectResult});

      } catch (error) {
        console.error(error);
        alert("Error occurred. Look at console for details");
      }
    },

    async createAdyenCheckout(session, clientKey) {
      const configuration = {
        clientKey: clientKey ,
        locale: "en_US",
        environment: "test", // change to live for production
        showPayButton: true,
        session: session,
        paymentMethodsConfiguration: {
          card: {
            hasHolderName: true,
            holderNameRequired: true,
            name: "Kreditkarte oder Debitkarte",
            amount: {
              value: 1000,
              currency: "EUR"
            }
          },
          paypal: {
            amount: {
              currency: "EUR",
              value: 1000
            },
            environment: "test",
            countryCode: "DE"   // Only needed for test. This will be automatically retrieved when you are in production.
          }
        },
        onPaymentCompleted: (result, component) => {
          console.log("result: " + result);
          this.handleServerResponse(result, component);
        },
        onError: (error, component) => {
          console.error(error.name, error.message, error.stack, component);
        }
      };

      return new AdyenCheckout(configuration);
    },

    async callServer(url, data) {
      const res = await fetch(url, {
        method: "POST",
        body: data ? JSON.stringify(data) : "",
        headers: {
          "Content-Type": "application/json",
        },
      });
      return await res.json();
    },

    async handleServerResponse(res, component) {
      if (res.action) {
        component.handleAction(res.action);
      } else {
        switch (res.resultCode) {
          case "Authorised":
            window.location.href = "/result/success";
            break;
          case "Pending":
          case "Received":
            window.location.href = "/result/pending";
            break;
          case "Refused":
            window.location.href = "/result/failed";
            break;
          default:
            window.location.href = "/result/error";
            break;
        }
      }
    }
  },
};
</script>
